   <script>
      var $input = $('#patient_number');
      $('#name_field').hide();
      $('.loading_data').hide();
      $("#load_data").hide();
      $input.keydown(function () {
         $("#load_data").show();
         $('.loading_data').hide();
         $('#name_field').hide();
      });

      function loadData(){
         $("#load_data").hide();
         $('#name_field').hide();
         $('.loading_data').show();
         $.ajax({
            type:'POST',
            url:'/account/appointment/get_patient_record',
            data: {_token: '<?php echo csrf_token() ?>', patient_number: $input.val()},
            dataType : 'json',
            success:function(data) {
               setTimeout(
                  function() 
                  {
                     $('.loading_data').hide();
                     $('#patient_name').val(data.patient_name);
                     $('#patient_id').val(data.patient_id);
                     $('#name_field').show();
                  }, 500
               );
            }
         });
      }
   </script>

   <script>
      function loadTemplate(){
         var value = $('select[name="template_id"]').val();
         var content = CKEDITOR.instances['report_content'];
         if(value == 0){
            content.setData("<p></p>");
         }else{
            $.ajax({
               type:'POST',
               url:'/account/get_report_template',
               data: {_token: '<?php echo csrf_token() ?>', template_id: value},
               dataType : 'json',
               success:function(data) {
                  content.setData(data.template);
               }
            });
            }
      }
   </script>
